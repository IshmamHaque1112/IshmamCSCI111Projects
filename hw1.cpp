#include <iostream>
using namespace std;
int main() {
    double feet,inch,weight_estimation,total_inches,height,base1,base2,area;
    cout << "Enter your height in whole feet minus surplus inches: ";
    cin >> feet;
    cout <<"Enter your remaining inches: ";
    cin >> inch;
    total_inches=((feet*12)+ inch);
    weight_estimation=((total_inches-60.0)*5.0)+110.0;
    cout << "Your estimated weight is in total:  "<<weight_estimation<< endl;
    cout << "To find area of a trapezoid, enter the height first,then press enter, then enter base 1,then press enter, then base 2, and press enter:  ";
    cin >> height;
    cin >> base1;
    cin >> base2;
    area=(1.0/2.0)*height*(base1+base2);
    cout << "Your trapezoid area is: "<<area<< endl;
    return 0;
}
